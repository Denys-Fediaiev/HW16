public class Node {
    private int key;
    private String value;
    private Node left;
    private Node right;

    public int getKey() {
        return getKey();
    }

    public void setKey() {
        this.key = key;
    }

    public int getValue() {
        return getValue();
    }

    public void setValue() {
        this.value = value;
    }

    public Node getLeft() {
        return getLeft();
    }

    public void setLeft() {
        this.left = left;
    }

    public Node getRight() {
        return getRight();
    }

    public void setRight() {
        this.right = right;
    }
}